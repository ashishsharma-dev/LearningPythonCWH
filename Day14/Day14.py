# Day 14 : If Else Elif Statements

#  You can drive or not : if else statement
a = int(input("Enter Your Age: \n"))

print(a > 18)
print(a < 18)
print(a <= 18)
print(a >= 18)
print(a == 18)

if (a > 18):
    print("You can drive")
else:
    print("You can't drive")

