#  Create a Calculator

a = int(input("Enter first number\n"))
b = int(input("Enter second number\n"))

print(f"{a} + {b} is equal to: {a+b}")
print(f"{a} - {b} is equal to: {a-b}")
print(f"{a} * {b} is equal to: {a*b}")
print(f"{a} ** {b} is equal to: {a**b}")
print(f"{a} / {b} is equal to: {a/b}")
print(f"{a} // {b} is equal to: {a//b}")
print(f"{a} % {b} is equal to: {a%b}")
